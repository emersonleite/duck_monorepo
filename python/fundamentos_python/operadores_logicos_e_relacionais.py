""" Operadores lógicos """

a = True

b = False

""" and """
print(a and b)

print(a & b)

c = a and b

print("'A' e 'B' são iguais é", c)

""" or """
print(a or b) 

print(a | b)

""" not """
print(not a)

print(not b)

""" Operadores relacionais """

print(5 > 3)

print(5 < 3)

print(5 <= 3)

print(5 >= 3)

print(5 == 3)

print(5 != 3)